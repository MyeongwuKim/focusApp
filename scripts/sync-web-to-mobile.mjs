import { mkdirSync, readdirSync, readFileSync, rmSync, statSync, writeFileSync, cpSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const webDistDir = path.resolve(__dirname, '../apps/web-ui/dist');
const mobileDistMirrorDir = path.resolve(__dirname, '../apps/mobile/web-dist');
const outPath = path.resolve(__dirname, '../apps/mobile/src/features/webui/embeddedWebUiBundle.ts');

const walkFiles = (dir, baseDir) => {
  const entries = readdirSync(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      files.push(...walkFiles(fullPath, baseDir));
      continue;
    }

    if (entry.isFile()) {
      const relativePath = path.relative(baseDir, fullPath).split(path.sep).join('/');
      files.push(relativePath);
    }
  }

  return files;
};

if (!statSync(webDistDir).isDirectory()) {
  throw new Error(`web-ui dist not found: ${webDistDir}`);
}

rmSync(mobileDistMirrorDir, { recursive: true, force: true });
mkdirSync(mobileDistMirrorDir, { recursive: true });
cpSync(webDistDir, mobileDistMirrorDir, { recursive: true });

const files = walkFiles(webDistDir, webDistDir).map((relativePath) => {
  const fullPath = path.join(webDistDir, relativePath);
  const contentBase64 = readFileSync(fullPath).toString('base64');

  return {
    path: relativePath,
    contentBase64,
  };
});

const fileContent = `// Auto-generated by scripts/sync-web-to-mobile.mjs
export type EmbeddedWebUiFile = {
  path: string;
  contentBase64: string;
};

export const embeddedWebUiFiles: EmbeddedWebUiFile[] = ${JSON.stringify(files)};
`;

writeFileSync(outPath, fileContent, 'utf8');
console.log(`Synced web dist -> ${mobileDistMirrorDir}`);
console.log(`Generated: ${outPath} (${files.length} files)`);
